get_num_int1 = int(input('Enter the first integer: '))
get_num_int2 = int(input('Enter the second integer: '))

sum = get_num_int1 + get_num_int2
difference = get_num_int1 - get_num_int2
product = get_num_int1 * get_num_int2
quotient = float(get_num_int1 / get_num_int2)
remainder = get_num_int1 % get_num_int2

print(f'Sum: {sum}')
print(f'Difference: {difference}')
print(f'Product: {product}')
print(f'Quotient: {quotient}')
print(f'Remainder: {remainder}')